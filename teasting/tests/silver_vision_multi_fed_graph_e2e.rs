fn permissive_cell(seed: &str, balance: u64) -> Cell {
    let key = test_key(seed);
    let mut cell = Cell::with_balance(key, token_id(), balance);
    cell.permissions = Permissions {
        send: AuthRequired::None,
        receive: AuthRequired::None,
        set_state: AuthRequired::None,
        set_permissions: AuthRequired::None,
        set_verification_key: AuthRequired::None,
        increment_nonce: AuthRequired::None,
        delegate: AuthRequired::None,
        access: AuthRequired::None,
    };
    cell
}

/// Build a ledger for Federation F1 (cells A, B).
fn make_f1_ledger() -> (Ledger, [CellId; 2]) {
    let mut ledger = Ledger::new();
    let mut ids = [CellId::from_bytes([0u8; 32]); 2];
    let seeds = ["F1:A-issuer", "F1:B-registry"];
    let balances = [1_000_000u64, 1_000_000];
    for i in 0..2 {
        let cell = permissive_cell(seeds[i], balances[i]);
        ids[i] = cell.id();
        ledger.insert_cell(cell).unwrap();
    }
    // Self-caps and forward cap A→B.
    for i in 0..2 {
        let agent = ledger.get_mut(&ids[i]).unwrap();
        agent.capabilities.grant(ids[i], AuthRequired::None);
        if i + 1 < 2 {
            agent.capabilities.grant(ids[i + 1], AuthRequired::None);
        }
    }
    (ledger, ids)
}

/// Build a ledger for Federation F2 (cells C, D).
fn make_f2_ledger() -> (Ledger, [CellId; 2]) {
    let mut ledger = Ledger::new();
    let mut ids = [CellId::from_bytes([0u8; 32]); 2];
    let seeds = ["F2:C-subscription", "F2:D-worker"];
    let balances = [5_000_000u64, 100_000];
    for i in 0..2 {
        let cell = permissive_cell(seeds[i], balances[i]);
        ids[i] = cell.id();
        ledger.insert_cell(cell).unwrap();
    }
    for i in 0..2 {
        let agent = ledger.get_mut(&ids[i]).unwrap();
        agent.capabilities.grant(ids[i], AuthRequired::None);
        if i + 1 < 2 {
            agent.capabilities.grant(ids[i + 1], AuthRequired::None);
        }
    }
    // D also needs a cap to C for Transfer.
    let d = ledger.get_mut(&ids[1]).unwrap();
    d.capabilities.grant(ids[0], AuthRequired::None);
    (ledger, ids)
}

// ---------------------------------------------------------------------------
