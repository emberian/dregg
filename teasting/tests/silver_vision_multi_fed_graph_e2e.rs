/// Full Silver Vision multi-federation E2E test.
///
/// See module-level documentation for the flow and the 8 verification stages.
#[test]
fn silver_vision_multi_fed_graph_e2e() {
    // ── Stage 1: F1 sub-chain (t1, t2) ──────────────────────────────────
    let (mut f1_ledger, f1_ids) = make_f1_ledger();
    let f1_id = fed_id(FED1_SEED);
    let mut f1_executor = TurnExecutor::new(ComputronCosts::default_costs());
    f1_executor.set_local_federation_id(f1_id);

    // Fee shares + AR/receipt consistency for multi-fed Silver (F1 subchain).
    let mut f1_prop = permissive_cell("mf-f1-proposer", 0);
    let f1_prop_id = f1_prop.id();
    f1_ledger.insert_cell(f1_prop).unwrap();
    f1_executor.set_proposer_cell(f1_prop_id);
    let mut f1_treas = permissive_cell("mf-f1-treasury", 0);
    let f1_treas_id = f1_treas.id();
    f1_ledger.insert_cell(f1_treas).unwrap();
    f1_executor.set_treasury_cell(f1_treas_id);

    let credential_value = *blake3::hash(b"multi-fed-silver-credential-v1").as_bytes();
    let name_value = *blake3::hash(b"alice.dregg.multifed").as_bytes();

    // t1: Cell A (F1) issues a credential.
    let pre_a = f1_ledger.get(&f1_ids[0]).unwrap().state.balance();
    let t1 = build_single_effect_turn(
        f1_ids[0],
        0,
        None,
        vec![], // genesis
        f1_ids[0],
        "issue_credential",
        Effect::SetField {
            cell: f1_ids[0],
            index: 0,
            value: credential_value,
        },
        "t1: A issues credential (F1)",
    );
    let t1_hash = t1.hash();
    let r1 = execute_or_panic(&f1_executor, &mut f1_ledger, &t1, "t1/A-issuer");

    // t2: Cell B (F1) registers, depends on t1.
    let pre_b = f1_ledger.get(&f1_ids[1]).unwrap().state.balance();
    let t2 = build_single_effect_turn(
        f1_ids[1],
        0,
        None,
        vec![t1_hash],
        f1_ids[1],
        "register_name",
        Effect::SetField {
            cell: f1_ids[1],
            index: 0,
            value: name_value,
        },
        "t2: B registers name (F1, depends on t1)",
    );
    let r2 = execute_or_panic(&f1_executor, &mut f1_ledger, &t2, "t2/B-registry");

    // F1 ledger effects must be visible.
    assert_eq!(
        f1_ledger.get(&f1_ids[0]).unwrap().state.fields[0],
        credential_value,
        "t1 effect must be visible on issuer"
    );
    assert_eq!(
        f1_ledger.get(&f1_ids[1]).unwrap().state.fields[0],
        name_value,
        "t2 effect must be visible on registry"
    );

    // The causal chain within F1 is enforced: t2 pins t1's turn hash.
    assert!(
        t2.depends_on.contains(&t1_hash),
        "t2 must depend on t1 by turn hash"
    );

    // Fee shares visible for F1 (2 turns): post shares in bals + will be in its AR merkle.
    assert_eq!(
        f1_ledger.get(&f1_prop_id).unwrap().state.balance(),
        300,
        "F1 proposer 300 shares"
    );
    assert_eq!(
        f1_ledger.get(&f1_treas_id).unwrap().state.balance(),
        180,
        "F1 treasury 180 shares"
    );

    // ── Stage 2: F2 sub-chain (t3, t4) ──────────────────────────────────
    let (mut f2_ledger, f2_ids) = make_f2_ledger();
    let f2_id = fed_id(FED2_SEED);
    let mut f2_executor = TurnExecutor::new(ComputronCosts::default_costs());
    f2_executor.set_local_federation_id(f2_id);

    // Fee shares for F2 subchain (symmetric to F1; exercises shares in its AR/receipts).
    let mut f2_prop = permissive_cell("mf-f2-proposer", 0);
    let f2_prop_id = f2_prop.id();
    f2_ledger.insert_cell(f2_prop).unwrap();
    f2_executor.set_proposer_cell(f2_prop_id);
    let mut f2_treas = permissive_cell("mf-f2-treasury", 0);
    let f2_treas_id = f2_treas.id();
    f2_ledger.insert_cell(f2_treas).unwrap();
    f2_executor.set_treasury_cell(f2_treas_id);

    // The cross-fed citation: C in F2 cites t2's receipt by hash.
    let t2_receipt_hash = r2.receipt_hash();
    let cross_fed_attestation = cross_fed_receipt_cite(&f2_ids[0], &f1_id, &t2_receipt_hash);

    // Encode the attestation in the turn's memo (synthetic — in production the
    // attestation would be a sovereign witness or a custom effect field; we use
    // memo here to keep the test within the existing surface without adding a
    // new Effect variant). The verifiable binding is in the attestation_data
    // itself, not in the turn memo.
    let cross_fed_memo = format!(
        "t3: C cites F1:t2 via UnilateralAttestation::Custom({:?})",
        cross_fed_attestation.attestation_data
    );

    // t3: Cell C (F2) publishes a subscription, citing t2 via the unilateral attestation.
    // The attestation data is bound into a SetField effect so the receipt's effects_hash
    // commits to the citation.
    let bounty_value = cross_fed_attestation.attestation_data; // encode the citation as field[0]
    let pre_c = f2_ledger.get(&f2_ids[0]).unwrap().state.balance();
    let t3 = build_single_effect_turn(
        f2_ids[0],
        0,
        None,
        vec![], // no local causal deps — the cross-fed dep is declared via attestation
        f2_ids[0],
        "publish_subscription",
        Effect::SetField {
            cell: f2_ids[0],
            index: 0,
            value: bounty_value, // the attestation_data IS the field value
        },
        &cross_fed_memo,
    );
    let t3_hash = t3.hash();
    let r3 = execute_or_panic(&f2_executor, &mut f2_ledger, &t3, "t3/C-subscription");

    // t4: Cell D (F2) transfers within F2, depends on t3.
    let bounty_amount: u64 = 200;
    let pre_d = f2_ledger.get(&f2_ids[1]).unwrap().state.balance();
    let t4 = build_single_effect_turn(
        f2_ids[1],
        0,
        None,
        vec![t3_hash],
        f2_ids[0], // target: C (bounty payer)
        "claim_subscription",
        Effect::Transfer {
            from: f2_ids[0],
            to: f2_ids[1],
            amount: bounty_amount,
        },
        "t4: D claims bounty from C (Transfer within F2, depends on t3)",
    );
    let r4 = execute_or_panic(&f2_executor, &mut f2_ledger, &t4, "t4/D-worker");

    // F2 ledger effects must be visible.
    assert_eq!(
        f2_ledger.get(&f2_ids[0]).unwrap().state.fields[0],
        bounty_value,
        "t3 effect (cross-fed citation as field) must be visible on C"
    );
    assert_eq!(
        f2_ledger.get(&f2_ids[1]).unwrap().state.balance(),
        pre_d + bounty_amount - 300,
        "D's balance must increase by bounty amount (after paying its turn fee)"
    );
    assert_eq!(
        f2_ledger.get(&f2_ids[0]).unwrap().state.balance(),
        pre_c - bounty_amount - 300,
        "C's balance must decrease by bounty amount (after paying its t3 turn fee)"
    );

    // t4 pins t3 by turn hash.
    assert!(
        t4.depends_on.contains(&t3_hash),
        "t4 must depend on t3 by turn hash"
    );

    // Fee shares visible for F2 (2 turns).
    assert_eq!(
        f2_ledger.get(&f2_prop_id).unwrap().state.balance(),
        300,
        "F2 proposer 300 shares"
    );
    assert_eq!(
        f2_ledger.get(&f2_treas_id).unwrap().state.balance(),
        180,
        "F2 treasury 180 shares"
    );

    // ── Stage 3: Build F1's AttestedRoot over {t1, t2} ───────────────────
    let f1_receipt_hashes: Vec<[u8; 32]> = [r1.receipt_hash(), r2.receipt_hash()].into();
    let (f1_root, f1_pk) =
        build_attested_root(&f1_ledger, 1, 1_716_600_000, &f1_receipt_hashes, FED1_SEED);

    assert!(
        f1_root.is_valid(&[f1_pk]),
        "F1's AttestedRoot must verify under its committee key"
    );
    assert!(
        f1_root.verify_receipt_stream(&f1_receipt_hashes),
        "F1's AttestedRoot must bind the t1+t2 receipt set"
    );

    // ── Stage 4: Build F2's AttestedRoot over {t3, t4} ───────────────────
    let f2_receipt_hashes: Vec<[u8; 32]> = [r3.receipt_hash(), r4.receipt_hash()].into();
    let (f2_root, f2_pk) =
        build_attested_root(&f2_ledger, 1, 1_716_600_001, &f2_receipt_hashes, FED2_SEED);

    assert!(
        f2_root.is_valid(&[f2_pk]),
        "F2's AttestedRoot must verify under its committee key"
    );
    assert!(
        f2_root.verify_receipt_stream(&f2_receipt_hashes),
        "F2's AttestedRoot must bind the t3+t4 receipt set"
    );

    // ── Stage 5: Both federations' STARK chains verify ────────────────────
    // Build linked receipt-chain views for F1 (t1+t2) and F2 (t3+t4) so the
    // replay_chain walk passes (see silver single-fed for rationale). The
    // per-step proofs + bindings are still real.
    let mut f1_chain = vec![build_replay_entry(
        r1.clone(),
        VmEffect::SetField {
            field_idx: 0,
            value: BabyBear::new(1),
        },
        pre_a,
    )];
    {
        let mut r = r2.clone();
        r.previous_receipt_hash = Some(f1_chain[0].receipt.receipt_hash());
        f1_chain.push(build_replay_entry(
            r,
            VmEffect::SetField {
                field_idx: 0,
                value: BabyBear::new(2),
            },
            pre_b,
        ));
    }
    let f1_verdict = replay_chain(&f1_chain);
    assert!(
        f1_verdict.overall_verified,
        "F1 STARK chain (t1+t2) must verify: {} (verdicts: {:?})",
        f1_verdict.summary, f1_verdict.per_entry
    );
    for (i, v) in f1_verdict.per_entry.iter().enumerate() {
        assert!(
            matches!(v, ReplayVerdict::Verified),
            "F1 entry {} must be Verified, got {:?}",
            i,
            v
        );
    }

    // Plain (unlinked) F2 entries for Stage 8 adversarial (which builds its own
    // view from them, per the original structure and comments).
    let f2_entries = vec![
        build_replay_entry(
            r3.clone(),
            VmEffect::SetField {
                field_idx: 0,
                value: BabyBear::new(3),
            },
            pre_c,
        ),
        build_replay_entry(
            r4.clone(),
            VmEffect::Transfer {
                amount: bounty_amount,
                direction: 0,
            },
            pre_d,
        ),
    ];

    let mut f2_chain = vec![build_replay_entry(
        r3.clone(),
        VmEffect::SetField {
            field_idx: 0,
            value: BabyBear::new(3),
        },
        pre_c,
    )];
    {
        let mut r = r4.clone();
        r.previous_receipt_hash = Some(f2_chain[0].receipt.receipt_hash());
        f2_chain.push(build_replay_entry(
            r,
            VmEffect::Transfer {
                amount: bounty_amount,
                direction: 0,
            },
            pre_d,
        ));
    }
    let f2_verdict = replay_chain(&f2_chain);
    assert!(
        f2_verdict.overall_verified,
        "F2 STARK chain (t3+t4) must verify: {} (verdicts: {:?})",
        f2_verdict.summary, f2_verdict.per_entry
    );

    // ── Stage 6: Cross-fed binding verification ───────────────────────────
    //
    // F2's verifier confirms:
    //   1. t2_receipt_hash appears in F1's AttestedRoot via receipt_stream_root.
    //   2. The UnilateralAttestation::Custom data on t3 encodes exactly
    //      blake3("dregg-cross-fed-receipt-cite-v1" || C.cell_id || f1_id || t2_receipt_hash).
    //
    // This is the structural F2-cites-F1 binding. In production the verifier
    // additionally checks that F1's AttestedRoot was obtained via an
    // authenticated channel and signed by F1's known committee — emulated
    // here by having F2's executor's `trusted_federation_roots` contain f1_root
    // (which it does via add_trusted_federation_root, exercised below).

    // Step 6a: t2_receipt_hash is in F1's attested receipt stream.
    assert!(
        f1_root.verify_receipt_stream(&f1_receipt_hashes),
        "t2_receipt_hash must appear in F1's attested receipt stream"
    );
    // Confirm t2's hash is actually in the set F1 attests.
    assert!(
        f1_receipt_hashes.contains(&t2_receipt_hash),
        "t2_receipt_hash must be in the F1 receipt hash list"
    );

    // Step 6b: the unilateral attestation on t3 correctly encodes the citation.
    assert!(
        verify_cross_fed_citation(&cross_fed_attestation, &f2_ids[0], &f1_id, &t2_receipt_hash),
        "UnilateralAttestation::Custom data must encode F1:t2 cross-fed citation"
    );

    // Step 6c: the field written by t3 is exactly the attestation_data,
    //          so the receipt's effects_hash commits to the cross-fed reference.
    assert_eq!(
        f2_ledger.get(&f2_ids[0]).unwrap().state.fields[0],
        cross_fed_attestation.attestation_data,
        "t3's SetField must commit the attestation_data into C's state"
    );

    // ── Stage 7: adversarial — tamper F1's AttestedRoot ──────────────────
    //
    // If F1's committee key changes or the root is forged, the verification
    // of "t2_receipt_hash ∈ F1's attested stream" must fail. We simulate
    // this by constructing a tampered root signed under a DIFFERENT key.
    let tampered_sk_bytes = test_key("tampered-committee");
    let tampered_sk = DalekSigningKey::from_bytes(&tampered_sk_bytes);
    let tampered_pk = PublicKey(tampered_sk.verifying_key().to_bytes());

    let tampered_stream_root = merkle_root_of_receipt_hashes(&f1_receipt_hashes);
    let mut tampered_f1_root = AttestedRoot::new_legacy(
        f1_ledger.root_cached(),
        1,
        1_716_600_000,
        Vec::new(),
        None,
        1,
    );
    tampered_f1_root.receipt_stream_root = Some(tampered_stream_root);
    let tmsg = tampered_f1_root.signing_message();
    let tsig = tampered_sk.sign(&tmsg).to_bytes();
    tampered_f1_root
        .quorum_signatures
        .push((tampered_pk, Signature(tsig)));

    // The tampered root must NOT verify under F1's known committee key.
    assert!(
        !tampered_f1_root.is_valid(&[f1_pk]),
        "tampered F1 root signed by wrong key MUST NOT verify under F1's committee"
    );

    // Also: if we forge the receipt list (swap t1 and t2 positions), the
    // stream root should not match the citation (since the Merkle root over
    // a permuted set is a different value for most inputs).
    // Note: for a 2-element set the position matters in our Merkle scheme.
    let swapped_hashes = vec![f1_receipt_hashes[1], f1_receipt_hashes[0]];
    // Only assert if hashes differ (which they always should — t1≠t2).
    if swapped_hashes != f1_receipt_hashes {
        let swapped_root = merkle_root_of_receipt_hashes(&swapped_hashes);
        let original_root_val = merkle_root_of_receipt_hashes(&f1_receipt_hashes);
        assert_ne!(
            swapped_root, original_root_val,
            "permuted receipt list MUST produce a different Merkle root"
        );
    }

    // ── Stage 8: adversarial — reorder F2's chain ────────────────────────
    //
    // Build a per-agent receipt chain view for F2 (chaining previous_receipt_hash
    // between entries) and verify it. Then swap entries; assert the chain-walk
    // rejects.
    let mut chain_view: Vec<ReplayEntry> = Vec::with_capacity(2);
    chain_view.push(f2_entries[0].clone());
    {
        let prev = chain_view[0].receipt.receipt_hash();
        let mut e2 = f2_entries[1].clone();
        e2.receipt.previous_receipt_hash = Some(prev);
        let rebuilt = build_replay_entry(
            e2.receipt,
            VmEffect::Transfer {
                amount: bounty_amount,
                direction: 0,
            },
            pre_d,
        );
        chain_view.push(rebuilt);
    }

    let chain_verdict = replay_chain(&chain_view);
    assert!(
        chain_verdict.overall_verified,
        "F2's canonical-order receipt chain must verify before tampering: {}",
        chain_verdict.summary
    );

    // Swap the two F2 entries — this breaks the previous_receipt_hash chain.
    let mut reordered = chain_view.clone();
    reordered.swap(0, 1);
    let reorder_verdict = replay_chain(&reordered);
    assert!(
        !reorder_verdict.overall_verified,
        "reordered F2 chain MUST be rejected by the chain-walk (verdicts: {:?})",
        reorder_verdict.per_entry
    );
}

