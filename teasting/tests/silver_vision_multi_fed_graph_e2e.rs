// ---------------------------------------------------------------------------
// AttestedRoot construction helper
// ---------------------------------------------------------------------------

fn build_attested_root(
    ledger: &Ledger,
    height: u64,
    timestamp: i64,
    receipt_hashes: &[[u8; 32]],
    fed_seed: &str,
) -> (AttestedRoot, PublicKey) {
    let sk = fed_sk(fed_seed);
    let pk = PublicKey(sk.verifying_key().to_bytes());
    let stream_root = merkle_root_of_receipt_hashes(receipt_hashes);

    let mut root =
        AttestedRoot::new_legacy(ledger.root_cached(), height, timestamp, Vec::new(), None, 1);
    root.receipt_stream_root = Some(stream_root);

    let msg = root.signing_message();
    let sig_bytes = sk.sign(&msg).to_bytes();
    root.quorum_signatures.push((pk, Signature(sig_bytes)));

    (root, pk)
}

// ---------------------------------------------------------------------------
// Cross-fed binding: canonical attestation preimage
//
// Cell C in F2 cites t2's receipt from F1 using a UnilateralAttestation
// with kind=Custom { kind_tag: CROSS_FED_RECEIPT_CITE }.
//
// The attestation_data preimage encodes:
//   blake3("dregg-cross-fed-receipt-cite-v1" || f1_federation_id || t2_receipt_hash)
//
// This domain-separates the cross-fed citation from any intra-fed
// UnilateralAttestation::Custom entry, and binds the citing cell to a
// *specific* federation's receipt — so a forged attestation from a
// different F1' cannot be substituted.
// ---------------------------------------------------------------------------

const CROSS_FED_RECEIPT_CITE_KIND_TAG: u32 = 0x0001; // stable tag for cross-fed receipt citations

fn cross_fed_receipt_cite(
    citing_cell_id: &CellId,
    source_federation_id: &[u8; 32],
    cited_receipt_hash: &[u8; 32],
) -> UnilateralAttestation {
    let mut hasher = blake3::Hasher::new();
    hasher.update(b"dregg-cross-fed-receipt-cite-v1");
    hasher.update(citing_cell_id.as_bytes());
    hasher.update(source_federation_id);
    hasher.update(cited_receipt_hash);
    UnilateralAttestation {
        kind: UnilateralAttestationKind::Custom {
            kind_tag: CROSS_FED_RECEIPT_CITE_KIND_TAG,
        },
        attestation_data: *hasher.finalize().as_bytes(),
    }
}

/// Verify that an attestation is a valid cross-fed receipt cite for the
/// given (citing_cell_id, source_federation_id, cited_receipt_hash) triple.
fn verify_cross_fed_citation(
    attestation: &UnilateralAttestation,
    citing_cell_id: &CellId,
    source_federation_id: &[u8; 32],
    cited_receipt_hash: &[u8; 32],
) -> bool {
    match &attestation.kind {
        UnilateralAttestationKind::Custom { kind_tag }
            if *kind_tag == CROSS_FED_RECEIPT_CITE_KIND_TAG => {}
        _ => return false,
    }
    let expected = cross_fed_receipt_cite(citing_cell_id, source_federation_id, cited_receipt_hash);
    attestation.attestation_data == expected.attestation_data
}

