/// Tampering the cited receipt hash must produce a different attestation_data,
/// so a verifier cannot be fooled into accepting a forged cross-fed citation.
#[test]
fn cross_fed_citation_is_receipt_hash_bound() {
    let cell_id = CellId::from_bytes([0xC1; 32]);
    let f1_id = fed_id(FED1_SEED);
    let real_receipt_hash = [0xAA; 32];
    let fake_receipt_hash = [0xBB; 32];

    let real_att = cross_fed_receipt_cite(&cell_id, &f1_id, &real_receipt_hash);
    let fake_att = cross_fed_receipt_cite(&cell_id, &f1_id, &fake_receipt_hash);

    assert_ne!(
        real_att.attestation_data, fake_att.attestation_data,
        "different receipt hashes MUST produce different attestation_data"
    );
    assert!(verify_cross_fed_citation(
        &real_att,
        &cell_id,
        &f1_id,
        &real_receipt_hash
    ));
    assert!(!verify_cross_fed_citation(
        &real_att,
        &cell_id,
        &f1_id,
        &fake_receipt_hash
    ));
}

/// Citing the same receipt from two different source federations produces
/// different attestation_data — a cross-fed forgery that substitutes a
/// different F1' cannot reuse F1's citation.
#[test]
fn cross_fed_citation_is_federation_id_bound() {
    let cell_id = CellId::from_bytes([0xC1; 32]);
    let f1_id = fed_id(FED1_SEED);
    let f1_prime_id = fed_id("some-other-federation");
    let receipt_hash = [0xAA; 32];

    let att_f1 = cross_fed_receipt_cite(&cell_id, &f1_id, &receipt_hash);
    let att_f1_prime = cross_fed_receipt_cite(&cell_id, &f1_prime_id, &receipt_hash);

    assert_ne!(
        att_f1.attestation_data, att_f1_prime.attestation_data,
        "different source federations MUST produce different attestation_data"
    );
    assert!(verify_cross_fed_citation(
        &att_f1,
        &cell_id,
        &f1_id,
        &receipt_hash
    ));
    assert!(!verify_cross_fed_citation(
        &att_f1,
        &cell_id,
        &f1_prime_id,
        &receipt_hash
    ));
}

/// Citing the same receipt from the same federation by two different cells
/// produces different attestation_data — so cell C cannot reuse cell C2's
/// citation.
#[test]
fn cross_fed_citation_is_citing_cell_bound() {
    let cell_c = CellId::from_bytes([0xC1; 32]);
    let cell_c2 = CellId::from_bytes([0xC2; 32]);
    let f1_id = fed_id(FED1_SEED);
    let receipt_hash = [0xAA; 32];

    let att_c = cross_fed_receipt_cite(&cell_c, &f1_id, &receipt_hash);
    let att_c2 = cross_fed_receipt_cite(&cell_c2, &f1_id, &receipt_hash);

    assert_ne!(
        att_c.attestation_data, att_c2.attestation_data,
        "different citing cells MUST produce different attestation_data"
    );
}

/// The UnilateralAttestation::Custom kind_tag is checked; a SelfStateTransition
/// attestation MUST NOT pass the cross-fed citation verifier even if the
/// attestation_data bytes were identical by accident.
#[test]
fn cross_fed_citation_rejects_wrong_kind() {
    let cell_id = CellId::from_bytes([0xC1; 32]);
    let f1_id = fed_id(FED1_SEED);
    let receipt_hash = [0xAA; 32];

    // Build a valid cross-fed citation.
    let att = cross_fed_receipt_cite(&cell_id, &f1_id, &receipt_hash);
    assert!(verify_cross_fed_citation(
        &att,
        &cell_id,
        &f1_id,
        &receipt_hash
    ));

    // Forge it with SelfStateTransition kind (same attestation_data bytes).
    let forged = UnilateralAttestation {
        kind: UnilateralAttestationKind::SelfStateTransition,
        attestation_data: att.attestation_data,
    };
    assert!(
        !verify_cross_fed_citation(&forged, &cell_id, &f1_id, &receipt_hash),
        "SelfStateTransition kind must be rejected by cross-fed citation verifier"
    );
}

/// F1's AttestedRoot must reject a receipt set that includes a forged receipt.
#[test]
fn f1_attested_root_rejects_forged_receipt() {
    let (mut f1_ledger, f1_ids) = make_f1_ledger();
    let f1_id = fed_id(FED1_SEED);
    let mut f1_executor = TurnExecutor::new(ComputronCosts::default_costs());
    f1_executor.set_local_federation_id(f1_id);

    let t1 = build_single_effect_turn(
        f1_ids[0],
        0,
        None,
        vec![],
        f1_ids[0],
        "issue_credential",
        Effect::SetField {
            cell: f1_ids[0],
            index: 0,
            value: [0xCC; 32],
        },
        "t1",
    );
    let r1 = execute_or_panic(&f1_executor, &mut f1_ledger, &t1, "t1");
    let t2 = build_single_effect_turn(
        f1_ids[1],
        0,
        None,
        vec![t1.hash()],
        f1_ids[1],
        "register_name",
        Effect::SetField {
            cell: f1_ids[1],
            index: 0,
            value: [0xDD; 32],
        },
        "t2",
    );
    let r2 = execute_or_panic(&f1_executor, &mut f1_ledger, &t2, "t2");

    let hashes: Vec<[u8; 32]> = [r1.receipt_hash(), r2.receipt_hash()].into();
    let (root, _pk) = build_attested_root(&f1_ledger, 1, 0, &hashes, FED1_SEED);

    assert!(root.verify_receipt_stream(&hashes));

    // Inject a forged receipt.
    let mut forged = hashes.clone();
    forged[1][0] ^= 0xFF;
    assert!(
        !root.verify_receipt_stream(&forged),
        "AttestedRoot must reject a forged receipt in the stream"
    );
}
