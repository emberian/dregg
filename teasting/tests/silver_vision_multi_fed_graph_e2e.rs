//! Silver Vision multi-federation graph end-to-end test.
//!
//! ## What this test demonstrates
//!
//! The existing `silver_vision_graph_e2e.rs` proves the single-federation
//! causal-graph + AttestedRoot binding story. This file extends that story
//! to two cooperating federations that are each sovereign — they maintain
//! separate ledgers, separate executors (each stamped with its federation_id),
//! and separate AttestedRoots — and shows that F2's cell C can cite F1's
//! t2 receipt by hash using a `UnilateralAttestation::Custom` binding, and
//! that a verifier can confirm the citation is sound.
//!
//! ## Flow (per the Silver Vision spec)
//!
//! ```text
//! Federation F1                           Federation F2
//! ─────────────────────────────────────   ─────────────────────────────────────
//! Cell A (issuer)  ──t1──► Cell B (registry) ──t2──┐
//!                                                    │
//!                        F2 learns F1's AttestedRoot │
//!                        covering t2                 │
//!                                                    ▼
//!                                           Cell C (subscription) ──t3──►
//!                                           (cites t2 via UnilateralAttestation::Custom)
//!                                                                          │
//!                                                                          ▼
//!                                           Cell D (worker) ──t4──► (Transfer within F2)
//! ```
//!
//! Turn ordering:
//!   t1 — Cell A (F1) issues a credential.  No causal deps.
//!   t2 — Cell B (F1) registers using A's credential.  depends_on = [t1].
//!   Cross-fed handoff: F1 builds an AttestedRoot covering t2; F2 learns it.
//!   t3 — Cell C (F2) cites t2's receipt-hash via UnilateralAttestation::Custom.
//!         depends_on = [].  The cross-fed binding is expressed by attaching the
//!         attestation data that encodes hash(t2_receipt) so F2's verifier can
//!         confirm the citation against F1's attested root.
//!   t4 — Cell D (F2) transfers within F2.  depends_on = [t3].
//!
//! ## Verification stages
//!
//!   Stage 1 — Execute F1's sub-chain (t1, t2) on F1's ledger; capture receipts.
//!   Stage 2 — Execute F2's sub-chain (t3, t4) on F2's ledger; capture receipts.
//!   Stage 3 — Build F1's AttestedRoot over {t1, t2} with receipt_stream_root.
//!   Stage 4 — Build F2's AttestedRoot over {t3, t4} with receipt_stream_root.
//!   Stage 5 — Both federations' chains verify under their own AttestedRoots.
//!   Stage 6 — Cross-fed binding: t3's UnilateralAttestation data encodes t2's
//!              receipt hash; F2's verifier confirms the hash appears in F1's
//!              attested receipt_stream_root (Merkle membership check).
//!   Stage 7 — Adversarial: tamper F1's AttestedRoot → t3's cross-fed binding rejects.
//!   Stage 8 — Adversarial: reorder F2's chain → F2's local replay rejects.
//!
//! ## What is synthetic and what is real
//!
//! * AttestedRoot is signed by a 1-of-1 synthetic committee (same as the
//!   single-fed test). The threshold/quorum machinery is identical to
//!   production; the committee size is 1 for test simplicity.
//! * The STARK proofs are real (generated by `generate_effect_vm_trace` +
//!   `stark::prove` and verified via `replay_chain`).
//! * The cross-fed receipt exchange is modelled in-process: we hand F2's
//!   executor the F1 AttestedRoot directly. In production this arrives via
//!   the wire protocol (`WireMessage`) and the executor's
//!   `add_trusted_federation_root`; the in-process path exercises the same
//!   data surface.
//! * The sibling cross-fed seam lane API was not available at test-write
//!   time. The test drives the canonical helpers directly and documents the
//!   missing seam below.
//!
//! ## Missing seam (sibling lane)
//!
//! The sibling cross-fed seam lane is expected to provide a type like:
//! ```
//! // pub fn lift_f1_receipt_to_f2(
//! //     f1_root: &AttestedRoot,
//! //     receipt_hash: [u8; 32],
//! // ) -> Result<CrossFedReceiptLift, LiftError>
//! ```
//! that encapsulates the Merkle-membership proof for `receipt_hash` in
//! `f1_root.receipt_stream_root`. Until that API is available, this test
//! performs the membership check inline via `f1_root.verify_receipt_stream`
//! (which re-derives the root from the full receipt-hash list — adequate for
//! an in-process integration test, but weaker than the production wire path
//! which would carry only the Merkle proof, not the full list).

