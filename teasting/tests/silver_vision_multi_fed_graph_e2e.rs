const FED1_SEED: &str = "silver-multi-fed-e2e:F1";
const FED2_SEED: &str = "silver-multi-fed-e2e:F2";

fn fed_id(seed: &str) -> [u8; 32] {
    *blake3::hash(format!("fed-id:{seed}").as_bytes()).as_bytes()
}

fn fed_sk(seed: &str) -> DalekSigningKey {
    let bytes = *blake3::hash(format!("fed-committee-sk:{seed}").as_bytes()).as_bytes();
    DalekSigningKey::from_bytes(&bytes)
}

fn test_key(name: &str) -> [u8; 32] {
    *blake3::hash(format!("silver-multi-fed-e2e:{name}").as_bytes()).as_bytes()
}

fn token_id() -> [u8; 32] {
    *blake3::hash(b"silver-multi-fed-e2e:token").as_bytes()
}

