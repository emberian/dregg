//! Canonical predicates exercised by the differential harness.
//!
//! Each predicate is defined twice:
//!
//! 1. Once via `#[pyana_caveat]` so the DSL emits the full backend zoo
//!    (`_check`, `_air_constraints`, `_datalog`, `_kimchi`, `_midnight_zkir`,
//!    `_sp1_guest`, plus the compile-time `{Name}P3Air` struct).
//! 2. Once via a [`PredicateSpec`] entry that captures (a) the predicate's
//!    canonical name, (b) its semantic shape encoded as
//!    [`PredicateBody`] so the kimchi/AIR/plonky3 simulators can render the
//!    same predicate into their own substrate, and (c) the curated input
//!    battery to drive through every backend.
//!
//! Why the dual definition? The Rust evaluator generated by `gen_rust` is the
//! ground truth — but the other backends emit topology (Kimchi gates, AIR
//! columns, ZKIR JSON) without an in-process verifier function. To
//! cross-check those backends we re-derive the same predicate semantically
//! using the IR-aligned helpers in `pyana_dsl_runtime::diff_witness` and the
//! shape-aware simulators in this crate. The dual definition is the price of
//! not requiring the SP1 toolchain or Midnight's proof server to run a test.

use std::collections::HashSet;

use pyana_dsl::pyana_caveat;
use pyana_dsl_runtime::ConstraintError;

// ============================================================================
// DSL caveat definitions (the canonical source of truth for each predicate).
// ============================================================================
//
// Pre-edition note: pyana_caveat parses the function body and emits the
// matching backends. The bodies below MUST stay simple enough that all
// backends accept them; if you add a body with no IR support in one backend,
// that backend will be reported as Skip(reason) downstream.

#[pyana_caveat]
fn diff_not_after(token_expiry: u64, current_time: u64) {
    require!(current_time <= token_expiry);
}

#[pyana_caveat]
fn diff_minimum_balance(balance: u64, threshold: u64) {
    require!(balance >= threshold);
}

#[pyana_caveat]
fn diff_exact_equal_u64(expected: u64, actual: u64) {
    require!(expected == actual);
}

#[pyana_caveat]
fn diff_distinct_u64(a: u64, b: u64) {
    require!(a != b);
}

#[pyana_caveat]
fn diff_exact_equal_bytes(expected: [u8; 32], actual: [u8; 32]) {
    require!(expected == actual);
}

#[pyana_caveat]
fn diff_distinct_bytes(a: [u8; 32], b: [u8; 32]) {
    require!(a != b);
}

#[pyana_caveat]
fn diff_conjunction(balance: u64, threshold: u64, sender: u64, receiver: u64) {
    require!(balance >= threshold);
    require!(sender != receiver);
}

#[pyana_caveat]
fn diff_window(low: u64, mid: u64, high: u64) {
    require!(low <= mid);
    require!(mid <= high);
}

#[pyana_caveat]
fn diff_set_member(allowed: &std::collections::HashSet<u64>, candidate: u64) {
    require!(allowed.contains(candidate));
}

#[pyana_caveat]
fn diff_set_member_and_floor(allowed: &std::collections::HashSet<u64>, candidate: u64, floor: u64) {
    require!(allowed.contains(candidate));
    require!(candidate >= floor);
}

#[pyana_caveat]
fn diff_triple_equal(a: u64, b: u64, c: u64) {
    require!(a == b);
    require!(b == c);
}

