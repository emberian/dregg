/// A single requirement comparable by all backends. Kept separate from
/// `pyana_dsl::ir` (which is internal to the proc-macro crate) so this crate
/// can stand alone.
#[derive(Clone)]
pub enum Requirement {
    LessEqualU64(u64, u64),
    GreaterEqualU64(u64, u64),
    EqualU64(u64, u64),
    NotEqualU64(u64, u64),
    EqualBytes32([u8; 32], [u8; 32]),
    NotEqualBytes32([u8; 32], [u8; 32]),
    Membership { set: HashSet<u64>, element: u64 },
}

impl std::fmt::Debug for Requirement {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::LessEqualU64(l, r) => write!(f, "LessEqualU64({l}, {r})"),
            Self::GreaterEqualU64(l, r) => write!(f, "GreaterEqualU64({l}, {r})"),
            Self::EqualU64(l, r) => write!(f, "EqualU64({l}, {r})"),
            Self::NotEqualU64(l, r) => write!(f, "NotEqualU64({l}, {r})"),
            Self::EqualBytes32(_, _) => write!(f, "EqualBytes32(.., ..)"),
            Self::NotEqualBytes32(_, _) => write!(f, "NotEqualBytes32(.., ..)"),
            Self::Membership { set, element } => {
                write!(f, "Membership(|set|={}, element={element})", set.len())
            }
        }
    }
}

impl Requirement {
    /// The IR-level truth of this requirement.
    pub fn holds(&self) -> bool {
        match self {
            Self::LessEqualU64(l, r) => l <= r,
            Self::GreaterEqualU64(l, r) => l >= r,
            Self::EqualU64(l, r) => l == r,
            Self::NotEqualU64(l, r) => l != r,
            Self::EqualBytes32(l, r) => l == r,
            Self::NotEqualBytes32(l, r) => l != r,
            Self::Membership { set, element } => set.contains(element),
        }
    }
}

/// The body of a predicate as a conjunction of requirements (all backends
/// fail-fast on the first unsatisfied requirement, matching how the
/// generated `{name}_check` is emitted).
#[derive(Debug, Clone)]
pub struct PredicateBody {
    pub requirements: Vec<Requirement>,
}

impl PredicateBody {
    pub fn holds(&self) -> bool {
        self.requirements.iter().all(|r| r.holds())
    }
}

/// One full case: a body bound to concrete inputs, plus a label for
/// disagreement reports.
pub struct PredicateCase {
    pub label: String,
    pub body: PredicateBody,
    /// Direct invocation of the `gen_rust` evaluator for this predicate's
    /// inputs. Returning `Ok(())` means "accept", `Err(_)` means "reject".
    pub rust_eval: Box<dyn Fn() -> Result<(), ConstraintError>>,
    /// Concrete parameter values keyed by parameter name. The Datalog
    /// evaluator binds variables from this map; the lint passes consult
    /// the keys to confirm the emitted artefacts reference every parameter.
    pub inputs: CaseInputs,
}

/// Concrete inputs to a predicate, keyed by parameter name. The names
/// here MUST match the parameter names declared on the `#[pyana_caveat]`
/// function that produced the emitted artefacts.
#[derive(Default, Clone)]
pub struct CaseInputs {
    pub u64s: Vec<(&'static str, u64)>,
    pub bytes: Vec<(&'static str, [u8; 32])>,
    pub sets: Vec<(&'static str, HashSet<u64>)>,
}

impl CaseInputs {
    pub fn new() -> Self {
        Self::default()
    }
    pub fn u64(mut self, name: &'static str, v: u64) -> Self {
        self.u64s.push((name, v));
        self
    }
    pub fn bytes(mut self, name: &'static str, v: [u8; 32]) -> Self {
        self.bytes.push((name, v));
        self
    }
    pub fn set(mut self, name: &'static str, v: HashSet<u64>) -> Self {
        self.sets.push((name, v));
        self
    }
}

/// One predicate definition: its canonical name, the IR-shape catalog of
/// its body (which all simulators consult), and the input battery.
pub struct PredicateSpec {
    pub name: &'static str,
    /// Lazy thunk producing the case battery. Owns its own `HashSet`s.
    pub cases: fn() -> Vec<PredicateCase>,
}

// ============================================================================
// Case batteries: each one binds the `#[pyana_caveat]`-generated `_check`
// against the same data fed to the IR-shape simulators downstream.
// ============================================================================

fn cases_not_after() -> Vec<PredicateCase> {
    // pyana-caveat signature: diff_not_after(token_expiry, current_time);
    // requirement: current_time <= token_expiry.
    let mk = |expiry: u64, now: u64| {
        let inputs = CaseInputs::new()
            .u64("token_expiry", expiry)
            .u64("current_time", now);
        let req = Requirement::LessEqualU64(now, expiry);
        (req, inputs)
    };
    let mut out = Vec::new();
    let scenarios = [
        ("expiry-in-future", 100u64, 50u64),
        ("expiry-equal", 100, 100),
        ("expiry-past", 100, 101),
        ("boundary-zero", 0, 0),
        ("boundary-max", u64::MAX, u64::MAX),
        ("near-overflow-reject", u64::MAX - 1, u64::MAX),
    ];
    for (label, expiry, now) in scenarios {
        let (req, inputs) = mk(expiry, now);
        out.push(PredicateCase {
            label: label.into(),
            body: PredicateBody {
                requirements: vec![req],
            },
            rust_eval: Box::new(move || diff_not_after_check(expiry, now)),
            inputs,
        });
    }
    out
}

fn cases_minimum_balance() -> Vec<PredicateCase> {
    // signature: (balance, threshold); requirement: balance >= threshold.
    let scenarios = [
        ("above-threshold", 500u64, 100u64),
        ("equal-threshold", 100, 100),
        ("below-threshold", 50, 100),
        ("zero-floor", 0, 0),
    ];
    scenarios
        .into_iter()
        .map(|(label, bal, thr)| PredicateCase {
            label: label.into(),
            body: PredicateBody {
                requirements: vec![Requirement::GreaterEqualU64(bal, thr)],
            },
            rust_eval: Box::new(move || diff_minimum_balance_check(bal, thr)),
            inputs: CaseInputs::new().u64("balance", bal).u64("threshold", thr),
        })
        .collect()
}

