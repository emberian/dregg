//! Poseidon2 hash function over BabyBear with proven parameters from Plonky3.
//!
//! Parameters sourced from `p3-baby-bear` (Plonky3 repository) with 128-bit
//! security proofs from the Poseidon2 paper (https://eprint.iacr.org/2023/323).
//!
//! Configuration:
//! - Field: BabyBear (p = 2^31 - 2^27 + 1)
//! - Width: 16 field elements
//! - S-box: x^7
//! - External (full) rounds: 8 (4 initial + 4 final)
//! - Internal (partial) rounds: 13
//! - External linear layer: MDSMat4 circulant [2,3,1,1] applied blockwise
//! - Internal linear layer: 1 + Diag(V) where V is optimized for BabyBear
//!
//! Round constants generated by the Grain LFSR with parameters:
//!   field_type=1, alpha=7 (exp_flag=0), n=31, t=16, R_F=8, R_P=13

